	-- fast jump to files
	{
		"ThePrimeagen/harpoon",
		lazy = true,
		dependencies = "nvim-lua/plenary.nvim",
		keys = {
			{
				"<leader>j",
				function()
					require("harpoon.ui").toggle_quick_menu()
				end,
